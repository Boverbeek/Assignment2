
public class storeAddress {
	String street;
	String city;
	String state;
	String zipcode;
	String country;
	
	public storeAddress (String street, String city, String state, String zipcode, String country) {
		
		this.street = street;
		this.city = city;
		this.state = state;
		this.zipcode = zipcode;
		this.country = country;
	}
}
